export function getDeliveryPrice(city: string): number {
  if (!city) return DEFAULT_DELIVERY_PRICE;
  const normalized = city
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9\s]/g, "")
    .replace(/\s+/g, " ");
  return deliveryPrices[normalized] ?? DEFAULT_DELIVERY_PRICE;
}

export function getDeliveryEstimate(subtotal: number): { label: string; amount: number | null } {
  if (subtotal >= FREE_DELIVERY_THRESHOLD) {
    return { label: "Free (over ₦100,000)", amount: 0 };
  }
  return { label: "Calculated at checkout", amount: null };
}
